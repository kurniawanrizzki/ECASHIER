package com.noscale.ecashier.models;

import java.util.List;

/**
 *
 * @author rizkikurniawan
 */
public class Supplier {
    
    private int id;
    private String name, address, createdAt, updatedAt;
    private User createdBy, updatedBy;
    private List<String> getContacts;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public User getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(User createdBy) {
        this.createdBy = createdBy;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public User getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(User updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

    public List<String> getGetContacts() {
        return getContacts;
    }

    public void setGetContacts(List<String> getContacts) {
        this.getContacts = getContacts;
    }
    
}
